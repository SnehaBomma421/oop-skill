public class typecasting2 {
	    public static void main(String[] args) {
	        Scanner sc = new Scanner(System.in);
	        System.out.print("Enter a decimal number: ");
	        double a = sc.nextDouble();
	        int b = (int) a; 
	        System.out.println("Integer value: " + b);
	    }
	}



