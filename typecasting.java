package skill1;

public class typecasting {
	    public static void main(String[] args) {
	        int a = 100;       
	        long b = a;  
	        float c = b; 
	        System.out.println("Integer value: " + a);
	        System.out.println("Long value: " + b);
	        System.out.println("Float value: " + c);
	    }
	}



